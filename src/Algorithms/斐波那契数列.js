// 最简单的写法  时间复杂度 O(2*n) 不完全二叉树
/**
 * 数组下标
 *                  6
 *          |               |
 *          5               4
 *      |       |        |     |
 *      4       3        3     2
 *   |     |   |  |     | |
 *   3     2   2  1     2 1
 * |   |
 * 2   1
 */

var _index = 1;
function fib(n) {
    _index++;
    return n < 2 ? 1 : fib(n -1) + fib(n - 2);
}

console.log(fib(12), _index); // 233 466

